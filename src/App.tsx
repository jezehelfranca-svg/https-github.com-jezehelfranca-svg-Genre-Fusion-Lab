// Format duration seconds to mm:ss
function formatTime(seconds: number): string {
  const mins = Math.floor(seconds / 60);
  const secs = seconds % 60;
  return `${mins}:${secs.toString().padStart(2, '0')}`;
}

// Extract a clean genre name from Markdown syntax
function extractGenreName(markdown: string, ingredients: string[]): string {
  const lines = markdown.split('\n');
  
  // Try pattern matching for common heading structures
  for (const line of lines) {
    const cleanLine = line.trim();
    if (!cleanLine) continue;
    
    // Look for markdown headers
    const headerMatch = cleanLine.match(/^(?:#+\s*|\*\*\s*|Genre Name:\s*)([^*#\n:]+)(?:\s*\*+|\s*#*|$)/i);
    if (headerMatch && headerMatch[1]) {
      const clean = headerMatch[1].replace(/^(Name of the Genre|New Genre|Genre Name|Name|Title):\s*/i, '').trim();
      if (clean && clean.length > 2 && clean.length < 50) {
        return clean.replace(/[#*`"]/g, '');
      }
    }
  }

  // Fallback to first line with bold formatting
  for (let i = 0; i < Math.min(8, lines.length); i++) {
    const boldMatch = lines[i].match(/\*\*([^*]+)\*\*/);
    if (boldMatch && boldMatch[1]) {
      const clean = boldMatch[1].replace(/^(Name of the Genre|New Genre|Genre Name|Name|Title):\s*/i, '').trim();
      if (clean && clean.length > 2 && clean.length < 50) {
        return clean.replace(/[#*`"]/g, '');
      }
    }
  }

  // Generative fallback from ingredients
  return ingredients.slice(0, 3).join(' + ');
}

// Extract the custom comma-separated summary generated by Gemini at the end of the text
function extractBriefSummary(text: string | null): string {
  if (!text) return "";
  
  // Try to match "### Consolidated Brief Summary" with or without hashes, case-insensitive
  const markerIndex = text.search(/#+\s*Consolidated Brief Summary/i);
  if (markerIndex !== -1) {
    const mainText = text.substring(markerIndex);
    const lines = mainText.split('\n');
    // Filter out the header and find the non-empty lines following it
    const contentLines = lines.slice(1).map(l => l.trim()).filter(l => l.length > 0 && !l.startsWith('#'));
    if (contentLines.length > 0) {
      return contentLines[0].replace(/^["']|["']$/g, ''); // strip outer quotes if any
    }
  }

  // Backup simple search for "Consolidated Brief Summary:" or "Brief Summary:"
  const backupRegex = /(?:Consolidated Brief Summary|Brief Summary):\s*(.*)/i;
  const match = text.match(backupRegex);
  if (match && match[1]) {
    return match[1].trim().replace(/^["']|["']$/g, '');
  }

  return "";
}

