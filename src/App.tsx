interface ParsedTrack {
  id: string;
  title: string;
  story: string;
  imagePrompt: string;
}

function parseEPTracks(markdown: string | null): ParsedTrack[] {
  if (!markdown) return [];
  const tracks: ParsedTrack[] = [];
  
  const tracklistMarker = markdown.search(/Fictional EP Tracklist/i);
  let trackSection = markdown;
  if (tracklistMarker !== -1) {
    trackSection = markdown.substring(tracklistMarker);
  }
  
  const lines = trackSection.split('\n');
  let currentTrack: Partial<ParsedTrack> | null = null;
  
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;
    
    const trackHeaderMatch = line.match(/^(?:####|###|\*\*|)\s*Track\s*(\d+)\s*:\s*(.+?)(?:\s*\*+|\s*#*|$)/i);
    if (trackHeaderMatch) {
      if (currentTrack && currentTrack.title) {
        tracks.push(currentTrack as ParsedTrack);
      }
      const num = trackHeaderMatch[1];
      let name = trackHeaderMatch[2].replace(/[#*`"]/g, '').trim();
      currentTrack = {
        id: num,
        title: name,
        story: "",
        imagePrompt: ""
      };
      continue;
    }
    
    if (currentTrack) {
      const storyMatch = line.match(/^(?:-\s*|\*\s*|)\*\*Story\s*&\s*Context\s*:\s*\*\*(.+)/i) || 
                         line.match(/^(?:-\s*|\*\s*|)\*\*Context\s*&\s*Story\s*:\s*\*\*(.+)/i) ||
                         line.match(/^(?:-\s*|\*\s*|)\*\*Story\s*:\s*\*\*(.+)/i);
      if (storyMatch) {
        currentTrack.story = storyMatch[1].trim();
        continue;
      }
      
      const promptMatch = line.match(/^(?:-\s*|\*\s*|)\*\*Visual\s*&\s*Lyrics\s*Prompt\s*:\s*\*\*(.+)/i) ||
                          line.match(/^(?:-\s*|\*\s*|)\*\*Visual\s*&\s*Lyrics\s*:\s*\*\*(.+)/i) ||
                          line.match(/^(?:-\s*|\*\s*|)\*\*Prompt\s*:\s*\*\*(.+)/i);
      if (promptMatch) {
        currentTrack.imagePrompt = promptMatch[1].trim();
        continue;
      }
      
      if (line.startsWith('-') || line.startsWith('*')) {
        const cleanContent = line.replace(/^[-*]\s*/, '').trim();
        if (cleanContent.toLowerCase().includes('story') || cleanContent.toLowerCase().includes('context')) {
          const textOnly = cleanContent.replace(/^\*\*.*?\*\*\s*/, '').trim();
          if (textOnly) currentTrack.story = textOnly;
        } else if (cleanContent.toLowerCase().includes('prompt') || cleanContent.toLowerCase().includes('visual')) {
          const textOnly = cleanContent.replace(/^\*\*.*?\*\*\s*/, '').trim();
          if (textOnly) currentTrack.imagePrompt = textOnly;
        }
      }
    }
  }
  
  if (currentTrack && currentTrack.title) {
    tracks.push(currentTrack as ParsedTrack);
  }
  
  if (tracks.length === 0) {
    let fallbackTitle = "Untitled Track";
    const songMatch = markdown.match(/(?:iconic song title|song title|track name|debut track):\s*\*\*?([^*#\n\r]+)/i);
    if (songMatch && songMatch[1]) {
      fallbackTitle = songMatch[1].replace(/[#*`"]/g, '').trim();
    }
    
    tracks.push({
      id: "1",
      title: fallbackTitle,
      story: "An ethereal and groundbreaking showcase track carrying the core signature of the synthesized recipe elements.",
      imagePrompt: `Cinematic album cover for '${fallbackTitle}', abstract musical geometric patterns, synthwave aesthetic, high-fidelity neon colors, negative space, 1:1 ratio`
    });
    tracks.push({
      id: "2",
      title: `${fallbackTitle} (Cosmic Remix)`,
      story: "An extended ambient exploration taking the central atmospheres deeper into multi-dimensional auditory space.",
      imagePrompt: `A cosmic portal opening in dark stellar dust, synth wave aesthetics, deep-space violet color palette, digital render, 1:1 ratio`
    });
    tracks.push({
      id: "3",
      title: "Synthesize the Void",
      story: "A live jam capturing the spontaneous fusion of raw sound wave oscillators and human atmospheric depth.",
      imagePrompt: `Vintage oscillator synth glowing in a dark room filled with vaporwave fog and colored laser lines, high-contrast digital art, retro look, 1:1 ratio`
    });
  } else {
    if (tracks.length < 2) {
      tracks.push({
        id: "2",
        title: `${tracks[0].title} (Ethereal Reverb Mix)`,
        story: "A slowed-down, dreamlike ambient translation of the lead track, designed for meditative state integration.",
        imagePrompt: `Dreamy cloudscape reflecting neon colors at dusk, soft cinematic lighting, surreal digital fantasy, minimal style, 1:1 ratio`
      });
    }
    if (tracks.length < 3) {
      tracks.push({
        id: "3",
        title: "Resonant Harmony",
        story: "An outro track celebrating the balanced frequencies of the recipe signature.",
        imagePrompt: `Perfectly centered abstract golden audio wave lines on an absolute pure matte black background, elegant professional award-winning layout, 1:1 ratio`
      });
    }
  }
  
  return tracks;
}

